void	ft_value_swap_l(long *a, long *b)
{
	long	c;

	c = *a;
	*a = *b;
	*b = c;
}

void	ft_value_swap_i(int *a, int *b)
{
	int	c;

	c = *a;
	*a = *b;
	*b = c;
}
