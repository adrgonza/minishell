int	ft_abs(int x)
{
	if (x < -0)
		x = -x;
	return (x);
}

int	ft_min(int a, int b)
{
	if (a < b)
		return (a);
	return (b);
}

int	ft_max(int a, int b)
{
	if (a < b)
		return (b);
	return (a);
}
